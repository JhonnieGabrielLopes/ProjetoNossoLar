    public void limparTela() {
        tfBuscarPessoaNome.setText("");
        cbBuscarPessoaCidade.setSelectedIndex(0);
        if (identificador == 2) {
            cbBuscarPessoaTipo.setSelectedIndex(2);
        } else {
            cbBuscarPessoaTipo.setSelectedIndex(0);
        }
        tabelaModelo.setRowCount(0);
    }

